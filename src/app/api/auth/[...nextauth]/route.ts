import NextAuth from 'next-auth'
import { NextAuthOptions } from 'next-auth'
import CredentialsProvider from 'next-auth/providers/credentials'
import LineProvider from 'next-auth/providers/line'
import { PrismaAdapter } from '@next-auth/prisma-adapter'
import { prisma } from '@/lib/prisma'
import bcrypt from 'bcryptjs'

export const authOptions: NextAuthOptions = {
  // ‡∏õ‡∏¥‡∏î adapter ‡∏ä‡∏±‡πà‡∏ß‡∏Ñ‡∏£‡∏≤‡∏ß‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏°‡∏µ database
  adapter: process.env.DATABASE_URL ? PrismaAdapter(prisma) : undefined,
  providers: [
    LineProvider({
      clientId: process.env.LINE_CLIENT_ID!,
      clientSecret: process.env.LINE_CLIENT_SECRET!,
      authorization: {
        params: {
          scope: 'profile openid'
        }
      }
    }),
    CredentialsProvider({
      name: 'credentials',
      credentials: {
        email: { label: 'Email', type: 'email' },
        password: { label: 'Password', type: 'password' }
      },
      async authorize(credentials) {
        if (!credentials?.email || !credentials?.password) {
          return null
        }

        const user = await prisma.user.findUnique({
          where: {
            email: credentials.email
          },
          include: {
            restaurant: true
          }
        })

        if (!user || !user.password) {
          return null
        }

        const isPasswordValid = await bcrypt.compare(
          credentials.password,
          user.password
        )

        if (!isPasswordValid) {
          return null
        }

        return {
          id: user.id,
          email: user.email,
          name: user.name,
          role: user.role,
          restaurantId: user.restaurant?.id
        }
      }
    })
  ],
  session: {
    strategy: 'jwt'
  },
  callbacks: {
    async redirect({ url, baseUrl }) {
      console.log('NextAuth redirect:', { url, baseUrl });
      
      // ‡∏ñ‡πâ‡∏≤‡πÄ‡∏õ‡πá‡∏ô LIFF ‡πÅ‡∏•‡∏∞‡πÑ‡∏°‡πà‡∏°‡∏µ specific callback ‡πÉ‡∏´‡πâ‡πÑ‡∏õ‡∏´‡∏ô‡πâ‡∏≤‡πÄ‡∏°‡∏ô‡∏π
      const urlObj = new URL(url, baseUrl);
      const isLiffCallback = urlObj.searchParams.has('liff') || 
                           url.includes('liff=true') || 
                           url.includes('menu/');
      
      // ‡∏´‡∏≤‡∏Å callback URL ‡∏°‡∏µ LIFF flag ‡∏´‡∏£‡∏∑‡∏≠‡πÄ‡∏õ‡πá‡∏ô‡∏Å‡∏≤‡∏£‡∏Å‡∏•‡∏±‡∏ö‡∏à‡∏≤‡∏Å LINE login
      if (isLiffCallback) {
        // ‡∏ñ‡πâ‡∏≤‡∏°‡∏µ callbackUrl ‡∏ó‡∏µ‡πà‡∏£‡∏∞‡∏ö‡∏∏‡πÅ‡∏•‡πâ‡∏ß ‡πÉ‡∏´‡πâ‡πÉ‡∏ä‡πâ‡∏ô‡∏±‡πâ‡∏ô
        if (url.includes('/menu/')) {
          return url.startsWith('/') ? `${baseUrl}${url}` : url;
        }
        // ‡πÑ‡∏°‡πà‡∏á‡∏±‡πâ‡∏ô‡πÉ‡∏´‡πâ‡πÑ‡∏õ‡∏´‡∏ô‡πâ‡∏≤‡πÄ‡∏°‡∏ô‡∏π‡∏´‡∏•‡∏±‡∏Å
        return `${baseUrl}/menu/cmcg20f2i00029hu8p2am75df`;
      }
      
      // ‡∏´‡∏•‡∏±‡∏á‡∏à‡∏≤‡∏Å LINE login ‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à‡πÅ‡∏•‡∏∞‡πÑ‡∏°‡πà‡∏°‡∏µ callback specific
      if (url === baseUrl || url === `${baseUrl}/`) {
        return `${baseUrl}/menu/cmcg20f2i00029hu8p2am75df`;
      }
      
      // Default redirect behavior
      if (url.startsWith('/')) return `${baseUrl}${url}`;
      if (new URL(url).origin === baseUrl) return url;
      return baseUrl;
    },
    async signIn({ user, account, profile }) {
      console.log('üîê SignIn callback triggered:', { 
        provider: account?.provider, 
        userId: account?.providerAccountId,
        userEmail: user.email,
        userName: user.name 
      });

      // ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö LINE provider ‡πÉ‡∏´‡πâ‡∏™‡∏£‡πâ‡∏≤‡∏á user ‡πÉ‡∏´‡∏°‡πà‡πÄ‡∏õ‡πá‡∏ô customer ‡πÄ‡∏™‡∏°‡∏≠
      if (account?.provider === 'line') {
        try {
          console.log('üì± LINE signIn data:', { user, account, profile });
          
          // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏Å‡∏≤‡∏£‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠ database
          if (!process.env.DATABASE_URL) {
            console.warn('‚ö†Ô∏è No DATABASE_URL found, using JWT-only session');
            return true; // ‡∏≠‡∏ô‡∏∏‡∏ç‡∏≤‡∏ï‡πÉ‡∏´‡πâ login ‡πÅ‡∏ï‡πà‡πÑ‡∏°‡πà‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å database
          }
          
          // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏°‡∏µ email ‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
          if (!user.email) {
            console.log('üìß LINE user has no email, creating mock email');
            // ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏°‡∏µ email ‡πÉ‡∏´‡πâ‡πÉ‡∏ä‡πâ LINE user ID ‡πÅ‡∏ó‡∏ô
            const lineUserId = account.providerAccountId;
            const mockEmail = `line_${lineUserId}@line.local`;
            
            // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏°‡∏µ user ‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà‡∏î‡πâ‡∏ß‡∏¢ LINE ID
            const existingUser = await prisma.user.findFirst({
              where: { 
                OR: [
                  { email: mockEmail },
                  { 
                    accounts: {
                      some: {
                        provider: 'line',
                        providerAccountId: lineUserId
                      }
                    }
                  }
                ]
              }
            });

            if (!existingUser) {
              console.log('üë§ Creating new LINE user with mock email');
              // ‡∏™‡∏£‡πâ‡∏≤‡∏á user ‡πÉ‡∏´‡∏°‡πà‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö LINE login ‡πÇ‡∏î‡∏¢‡πÉ‡∏ä‡πâ mock email
              await prisma.user.create({
                data: {
                  email: mockEmail,
                  name: user.name || 'LINE User',
                  image: user.image,
                  role: 'USER',
                  emailVerified: new Date()
                }
              });
              console.log('‚úÖ User created successfully');
            } else {
              console.log('üë§ Existing LINE user found');
            }
            return true;
          }
          
          // ‡∏ñ‡πâ‡∏≤‡∏°‡∏µ email ‡πÉ‡∏´‡πâ‡∏ó‡∏≥‡∏á‡∏≤‡∏ô‡∏õ‡∏Å‡∏ï‡∏¥
          const existingUser = await prisma.user.findUnique({
            where: { email: user.email }
          });

          if (!existingUser) {
            console.log('üë§ Creating new LINE user with email');
            // ‡∏™‡∏£‡πâ‡∏≤‡∏á user ‡πÉ‡∏´‡∏°‡πà‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö LINE login
            await prisma.user.create({
              data: {
                email: user.email,
                name: user.name || 'LINE User',
                image: user.image,
                role: 'USER',
                emailVerified: new Date()
              }
            });
            console.log('‚úÖ User created successfully');
          } else {
            console.log('üë§ Existing user found');
          }
          return true;
        } catch (error) {
          console.error('‚ùå Error creating LINE user:', error);
          
          // ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏°‡∏µ database ‡πÉ‡∏´‡πâ return true ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏ä‡πâ JWT session
          if ((error as Error).message?.includes('Database') || !process.env.DATABASE_URL) {
            console.warn('‚ö†Ô∏è Database error, falling back to JWT-only session');
            return true;
          }
          
          return false;
        }
      }
      
      return true;
    },
    async jwt({ token, user, account }) {
      if (user) {
        token.role = user.role
        token.restaurantId = user.restaurantId
      }
      
      // ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö LINE users ‡πÉ‡∏´‡πâ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏à‡∏≤‡∏Å database
      if (account?.provider === 'line') {
        try {
          let dbUser = null;
          
          if (token.email) {
            // ‡∏•‡∏≠‡∏á‡∏´‡∏≤‡∏î‡πâ‡∏ß‡∏¢ email ‡∏Å‡πà‡∏≠‡∏ô
            dbUser = await prisma.user.findUnique({
              where: { email: token.email },
              include: { restaurant: true }
            });
          }
          
          // ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡πÄ‡∏à‡∏≠ ‡πÉ‡∏´‡πâ‡∏•‡∏≠‡∏á‡∏´‡∏≤‡∏î‡πâ‡∏ß‡∏¢ LINE account
          if (!dbUser && account.providerAccountId) {
            dbUser = await prisma.user.findFirst({
              where: {
                accounts: {
                  some: {
                    provider: 'line',
                    providerAccountId: account.providerAccountId
                  }
                }
              },
              include: { restaurant: true }
            });
          }
          
          if (dbUser) {
            token.role = dbUser.role;
            token.restaurantId = dbUser.restaurant?.id;
            // ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï email ‡πÉ‡∏ô token ‡πÉ‡∏´‡πâ‡∏ï‡∏£‡∏á‡∏Å‡∏±‡∏ö database
            token.email = dbUser.email;
          }
        } catch (error) {
          console.error('Error fetching user from database:', error);
        }
      }
      
      return token
    },
    async session({ session, token }) {
      if (token) {
        session.user.id = token.sub!
        session.user.role = token.role as string
        session.user.restaurantId = token.restaurantId as string
      }
      return session
    }
  },
  pages: {
    signIn: '/auth/signin',
    error: '/auth/error'
  }
}

const handler = NextAuth(authOptions)

export { handler as GET, handler as POST } 